package ex1;

/**
 * La classe {@code Calcul} fournit des méthodes pour effectuer des calculs arithmétiques simples.
 */
public class Calcul {

    /**
     * Additionne deux nombres entiers.
     *
     * @param a le premier nombre entier
     * @param b le deuxième nombre entier
     * @return la somme des deux nombres
     */
    public int ajouter(int a, int b) {
        return a + b;
    }

    /**
     * Soustrait le deuxième nombre entier du premier.
     *
     * @param a le premier nombre entier
     * @param b le deuxième nombre entier
     * @return la différence entre le premier et le deuxième nombre
     */
    public int soustraire(int a, int b) {
        return a - b;
    }
}
